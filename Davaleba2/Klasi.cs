using System;
using System.Collections.Generic;
using System.Text;

namespace Davaleba2
{
    class Klasi
    {
        public int a,b;

        public Klasi(int a,int b)
        {
            this.a = a;
            this.b = b;
        }

        public int GetSum()
        {
            return a + b;
        }
    }
}
